# float - decimal/float numbers

# define a variable to hold a float/decimal value
pi = 3.1415926535897932384626433832795028841971693

# data type of the variable - pi
print("Pi is of type {}".format(type(pi)))

# default - 15 decimal places are shown - 3.141592653589793
print("Value of Pi {}".format(pi))

# controlling the decimal places - to format for 2 decimal places :2f
print("Value of Pi {:.2f}".format(pi))



